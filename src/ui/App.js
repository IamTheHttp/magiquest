class App extends React.Component {
  constructor(props) {
    super(props);
    this.state = {
      mapCanvasEl: null,
      minimapCanvasEl: null
    };
    
    // app starts game...
    setTimeout(() => {
      this.startGame();
    }, 1000);
    
    
    this.registerEvents();
  }
  
  initGameCanvas() {
    return new GameCanvas({
      mapHeight,
      mapWidth,
      viewHeight,
      viewWidth,
      onViewMapMove: (dataObj) => {
      },
      onViewMapClick: (dataObj) => {
      },
      onMiniMapClick:() => {
        this.game.requestBackgroundRender();
      },
      onMiniMapMove:() => {
      }
    }).getNewCanvasPairs({
      getMapRef: (API, el) => {
        API.addLayer('background');
        this.setState({
          mapAPI: API
        });
      },
      getMiniRef: (API, el) => {
        this.setState({
          minimapAPI: API
        });
      }
    });
  }
  
  initGameLoop() {
    return new GameLoop({
      tileMap,
      getMapAPI: () => {
        return this.state.mapAPI;
      },
      getMinimapAPI: () => {
        return this.state.minimapAPI;
      },
      viewSize: {
        viewHeight,
        viewWidth,
        mapHeight,
        mapWidth
      }
    });
  }
  
  startGame() {
    let {map, minimap} = this.initGameCanvas();
    this.game = this.initGameLoop();
    
    this.setState({
      map,
      minimap
    });
  }
  
  
  registerEvents() {
    document.body.addEventListener('keyup', (event) => {
      let map = {
        37: 'left',
        38: 'up',
        39: 'right',
        40: 'down'
      };
      
      let code = event.which || event.keyCode || event.code;
      let direction = map[code];
      
      this.game.dispatchAction({
        name: MOVE_ACTION,
        direction
      });
    });
  }
  
  render() {
    return (
      <div>
        <div className='canvas-main-container'>
          {this.state.map}
        </div>
        <div className='canvas-minimap-container'>
          {this.state.minimap}
        </div>
      </div>
    );
  }
}


export default App;